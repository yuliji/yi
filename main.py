# 					䷅	䷆	䷇	䷈	䷉	䷊	䷋	䷌	䷍	䷎	䷏
# U+4DDx	䷐	䷑	䷒	䷓	䷔	䷕	䷖	䷗	䷘	䷙	䷚	䷛	䷜	䷝	䷞	䷟
