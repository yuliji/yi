    biangua = ['', '', '', '', '', '']
    for i in range(6):
        y = qigua()
        gua[i] = y
        if y == '⚋' or y == '⚊':
            bengua[i] = y
            biangua[i] = y
        elif y == 'o':
            bengua[i] = '⚊'
            biangua[i] = '⚋'
        elif y == 'x':
            bengua[i] = '⚋'
